
public class Circle {
	private int x; // X coordinate of the circle's center
	private int y; // Y coordinate of the circle's center
	private int r; // Radius of the circle
	public Circle(int x, int y, int r){
		this.x = x;
		this.y = y;
		this.r = r;
	}
	public int getX(){
		return x;
	}
	public int getY(){
		return y;
	}
	public int getR(){
		return r;
	}
}
