public class punto2D {
    
    private int x,y;
    
    /*
    *   PUNTO 2D
    */
    public punto2D(int x1, int y1){
        x = x1;
        y = y1;
    }
    
    
                //GET
    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
    
                //SET
    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    

    
    
    
}